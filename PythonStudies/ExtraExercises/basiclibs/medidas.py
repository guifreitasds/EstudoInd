def metertocenti(m):
    centimeters = m*100
    return centimeters

def circleArea(r, pi=3.14):
    area = pi*(r*r)
    return area

def squareArea(l):
    area = l*l
    return area

def area(b, h):
    area = b*h
    return area

def FahtoCelsius(f):
    celsius = ((f-32)/1.8)
    return celsius

def CelsiustoFah(c):
    fahrenheit = ((c*1.8)+32)
    return fahrenheit

public class suceedAndanteceed {
	public static void main (String [] args) {
		int number = 1329;
		int succeed;
		int antecceed;
		
		succeed = (number + 1);
		antecceed = (number - 1);
		
		System.out.println("O antecessor do número é: " + antecceed);
		System.out.println("O sucessor do número é: " + succeed);
	}

}
